#include <stdio.h>

int main()
{
    int a[]={34,232,23};
    printf("The value of a[0] is %d \n",a[0]);
    printf("The value of a[1] is %d \n",a[1]);
    printf("The value of a[2] is %d \n",a[2]);
    printf("\n");
    
    float b[]={34.4,232.5,23.6};
    printf("The value of b[0] is %f \n",b[0]);
    printf("The value of b[1] is %f \n",b[1]);
    printf("The value of b[2] is %f \n",b[2]);
    printf(" \n");

    char harry[]={'o','m','p'};
    printf("The value of harry[0] is %c \n",harry[0]);
    printf("The value of harry[1] is %c \n",harry[1]);
    printf("The value of harry[2] is %c \n",harry[2]);
    return 0;
}
